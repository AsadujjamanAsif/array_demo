public class loop_for_each {
    public static void main(String[] args) {

        String[] names = new String[4];
        names[0] = "asif";
        names[1] = "Ali";
        names[2] = "khan";
        names[3] = "mim";
        for (int i = 0; i < names.length; i++) {
            System.out.println(names[i]);

        }
    }
}
